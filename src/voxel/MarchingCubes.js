function hash3(x, y, z) {
  let h = (x * 374761393 + y * 668265263 + z * 1274126177) | 0;
  h = ((h ^ (h >> 13)) * 1274126177) | 0;
  return ((h & 0x7fffffff) / 0x7fffffff) * 2 - 1; // [-1, 1]
}

/**
 * March a padded 18x18x18 chunk and extract the isosurface.
 * @param {Uint8Array} paddedData - 18x18x18x2 voxel data with borders
 * @param {number} surfaceThreshold - density threshold (128)
 * @param {Float32Array} materialColors - flat RGB array per material
 * @param {Float32Array} materialEmissive - flat RGB emissive array per material
 * @param {number} chunkWorldX - world-space X offset of this chunk
 * @param {number} chunkWorldY - world-space Y offset of this chunk
 * @param {number} chunkWorldZ - world-space Z offset of this chunk
 * @returns {{ positions: Float32Array, normals: Float32Array, colors: Float32Array }}
 */
export function marchChunk(paddedData, surfaceThreshold, materialColors, materialEmissive, chunkWorldX, chunkWorldY, chunkWorldZ) {
  const positions = [];
  const normals = [];
  const colors = [];

  // Iterate over the 16x16x16 interior cubes
  // In the padded grid, the interior starts at index 1 and ends at 16
  for (let pz = 1; pz <= CHUNK; pz++) {
    for (let py = 1; py <= CHUNK; py++) {
      for (let px = 1; px <= CHUNK; px++) {
        // Sample the 8 corners of this cube
        const densities = new Float32Array(8);
        const materials = new Uint8Array(8);

        for (let v = 0; v < 8; v++) {
          const vx = px + VERTEX_OFFSETS[v][0];
          const vy = py + VERTEX_OFFSETS[v][1];
          const vz = pz + VERTEX_OFFSETS[v][2];
          const idx = (vx + vy * PADDED + vz * PADDED * PADDED) * VOXEL_BYTES;
          densities[v] = paddedData[idx];
          materials[v] = paddedData[idx + 1];
        }

        // Build the cube index
        let cubeIndex = 0;
        for (let v = 0; v < 8; v++) {
          if (densities[v] >= surfaceThreshold) {
            cubeIndex |= (1 << v);
          }
        }

        if (cubeIndex === 0 || cubeIndex === 255) continue;

        const edgeMask = EDGE_TABLE[cubeIndex];
        const edgeVerts = new Float32Array(12 * 3); // x,y,z per edge
        const edgeMats = new Uint8Array(12);

        // Interpolate vertices along intersected edges
        for (let e = 0; e < 12; e++) {
          if (!(edgeMask & (1 << e))) continue;

          const [v0, v1] = EDGE_VERTICES[e];
          const d0 = densities[v0];
          const d1 = densities[v1];

          // Linear interpolation factor
          let t = 0.5;
          if (Math.abs(d1 - d0) > 0.001) {
            t = (surfaceThreshold - d0) / (d1 - d0);
          }
          t = Math.max(0, Math.min(1, t));

          const off0 = VERTEX_OFFSETS[v0];
          const off1 = VERTEX_OFFSETS[v1];

          edgeVerts[e * 3]     = px + off0[0] + (off1[0] - off0[0]) * t - 1;
          edgeVerts[e * 3 + 1] = py + off0[1] + (off1[1] - off0[1]) * t - 1;
          edgeVerts[e * 3 + 2] = pz + off0[2] + (off1[2] - off0[2]) * t - 1;

          // Material: use the solid-side vertex's material
          edgeMats[e] = d0 >= surfaceThreshold ? materials[v0] : materials[v1];
        }

        // Generate triangles
        const triRow = TRI_TABLE[cubeIndex];
        for (let t = 0; t < triRow.length - 1; t += 3) {
          const e0 = triRow[t];
          const e1 = triRow[t + 1];
          const e2 = triRow[t + 2];
          if (e0 === -1) break;

          // Positions
          const ax = edgeVerts[e0 * 3], ay = edgeVerts[e0 * 3 + 1], az = edgeVerts[e0 * 3 + 2];
          const bx = edgeVerts[e1 * 3], by = edgeVerts[e1 * 3 + 1], bz = edgeVerts[e1 * 3 + 2];
          const cx = edgeVerts[e2 * 3], cy = edgeVerts[e2 * 3 + 1], cz = edgeVerts[e2 * 3 + 2];

          positions.push(ax, ay, az, bx, by, bz, cx, cy, cz);

          // Face normal (cross product)
          const ux = bx - ax, uy = by - ay, uz = bz - az;
          const vx = cx - ax, vy = cy - ay, vz = cz - az;
          let nx = uy * vz - uz * vy;
          let ny = uz * vx - ux * vz;
          let nz = ux * vy - uy * vx;
          const len = Math.sqrt(nx * nx + ny * ny + nz * nz) || 1;
          nx /= len; ny /= len; nz /= len;
          normals.push(nx, ny, nz, nx, ny, nz, nx, ny, nz);

          // Vertex colors with per-vertex noise
          for (const eIdx of [e0, e1, e2]) {
            const mat = edgeMats[eIdx];
            const worldVx = chunkWorldX + edgeVerts[eIdx * 3];
            const worldVy = chunkWorldY + edgeVerts[eIdx * 3 + 1];
            const worldVz = chunkWorldZ + edgeVerts[eIdx * 3 + 2];
            const noise = hash3(
              Math.floor(worldVx * 4),
              Math.floor(worldVy * 4),
              Math.floor(worldVz * 4)
            ) * 0.06;

            const mci = mat * 3;
            const baseR = materialColors[mci] + noise;
            const baseG = materialColors[mci + 1] + noise;
            const baseB = materialColors[mci + 2] + noise;

            // Add emissive contribution
            const emR = materialEmissive[mci];
            const emG = materialEmissive[mci + 1];
            const emB = materialEmissive[mci + 2];

            colors.push(
              Math.max(0, Math.min(1, baseR + emR)),
              Math.max(0, Math.min(1, baseG + emG)),
              Math.max(0, Math.min(1, baseB + emB))
            );
          }
        }
      }
    }
  }

  return {
    positions: new Float32Array(positions),
    normals: new Float32Array(normals),
    colors: new Float32Array(colors),
  };
}
